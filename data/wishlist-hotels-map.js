// Wishlist Shared Utility Functions

function getCurrentUser() {
  try {
    return JSON.parse(localStorage.getItem("currentUser") || "null");
  } catch (error) {
    return null;
  }
}

function readStorageArray(key) {
  try {
    const data = JSON.parse(localStorage.getItem(key) || "[]");
    return Array.isArray(data) ? data : [];
  } catch (error) {
    return [];
  }
}

function getFavoriteHotels() {
  return readStorageArray("favoriteHotels");
}

function formatBookingTime(date) {
  const pad = number => String(number).padStart(2, "0");
  return `${pad(date.getDate())}/${pad(date.getMonth() + 1)}/${date.getFullYear()} ${pad(date.getHours())}:${pad(date.getMinutes())}`;
}

function formatCurrencyVND(value) {
  return Math.max(0, Math.round(Number(value) || 0)).toLocaleString("vi-VN") + "₫";
}

function getHotelKeyFromHref(href) {
  if (!href) return "";
  const parts = href.split('/');
  const filename = parts[parts.length - 1];
  return filename.toLowerCase().trim();
}

function isHotelFavorite(hotelKey, currentUser) {
  if (!currentUser) return false;
  const hotelData = window.WISHLIST_HOTEL_MAP[hotelKey];
  if (!hotelData) return false;
  
  return getFavoriteHotels().some(function(item) {
    const belongsToUser = (item.userEmail && currentUser.email && item.userEmail === currentUser.email) ||
                          (item.userPhone && currentUser.phone && item.userPhone === currentUser.phone);
    return belongsToUser && 
           item.hotelCode === hotelData.hotelCode && 
           item.roomName === hotelData.roomName;
  });
}

function toggleHotelFavorite(hotelKey) {
  const currentUser = getCurrentUser();
  if (!currentUser) {
    alert("Bạn cần đăng nhập để lưu khách sạn yêu thích.");
    window.location.href = "../user/login.html";
    return null;
  }

  const hotelData = window.WISHLIST_HOTEL_MAP[hotelKey];
  if (!hotelData) {
    console.error("Hotel data not found for key: " + hotelKey);
    return null;
  }

  const favorites = getFavoriteHotels();
  const existingIndex = favorites.findIndex(function(item) {
    const belongsToUser = (item.userEmail && currentUser.email && item.userEmail === currentUser.email) ||
                          (item.userPhone && currentUser.phone && item.userPhone === currentUser.phone);
    return belongsToUser && 
           item.hotelCode === hotelData.hotelCode && 
           item.roomName === hotelData.roomName;
  });

  if (existingIndex >= 0) {
    favorites.splice(existingIndex, 1);
    localStorage.setItem("favoriteHotels", JSON.stringify(favorites));
    return false; // removed
  }

  favorites.push({
    userEmail: currentUser.email || "",
    userPhone: currentUser.phone || "",
    hotelCode: hotelData.hotelCode,
    hotelName: hotelData.hotelName,
    roomName: hotelData.roomName || "Phòng Đôi",
    location: hotelData.location,
    roomPrice: formatCurrencyVND(hotelData.price) + " / đêm",
    basePricePerNight: hotelData.price,
    imageUrl: hotelData.imageUrl,
    hotelUrl: hotelData.hotelUrl,
    savedAt: formatBookingTime(new Date())
  });

  localStorage.setItem("favoriteHotels", JSON.stringify(favorites));
  return true; // added
}

function initWishlistOnGrid() {
  const currentUser = getCurrentUser();
  
  document.querySelectorAll(".hotel-card").forEach(function(card) {
    const href = card.getAttribute("href");
    const hotelKey = getHotelKeyFromHref(href);
    const button = card.querySelector(".hc-fav, .hlc-fav");
    if (!button) return;

    const isSaved = isHotelFavorite(hotelKey, currentUser);
    const icon = button.querySelector("i");
    
    if (icon) {
      icon.classList.toggle("far", !isSaved);
      icon.classList.toggle("fas", isSaved);
    }
    button.classList.toggle("saved", isSaved);
  });
}

function handleCardFavClick(event, buttonElement) {
  event.preventDefault();
  event.stopPropagation();
  
  const card = buttonElement.closest(".hotel-card");
  if (!card) return;

  const href = card.getAttribute("href");
  const hotelKey = getHotelKeyFromHref(href);

  const result = toggleHotelFavorite(hotelKey);
  if (result === null) {
    // Redirected
    return;
  }

  const icon = buttonElement.querySelector("i");
  if (icon) {
    icon.classList.toggle("far", !result);
    icon.classList.toggle("fas", result);
  }
  buttonElement.classList.toggle("saved", result);
}
